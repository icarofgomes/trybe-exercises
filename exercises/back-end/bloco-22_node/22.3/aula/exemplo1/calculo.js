const soma = (valorA, valorB) => {
    return valorA + valorB;
}

const subtracao = (valorA, valorB) => {
    return valorA - valorB;
}

const multiplicacao = (valorA, valorB) => {
    return valorA * valorB;
}

const divisao = (valorA, valorB) => {
    return valorA / valorB;
}

module.exports = { soma, subtracao, multiplicacao, divisao };
